# Импортируем класс Path из стандартного модуля pathlib для удобной работы с путями к файлам и папкам.
from pathlib import Path


# Задаём имя итогового файла, в который будет собран весь код проекта.
OUTPUT_FILE_NAME = "code_dump.txt"


# Задаём набор папок, которые не нужно обходить при поиске Python-файлов.
EXCLUDED_DIRS = {
    "__pycache__",      # Исключаем служебные папки Python с байт-кодом.
    ".git",            # Исключаем внутреннюю папку Git-репозитория.
    ".venv",           # Исключаем виртуальное окружение с зависимостями проекта.
    "venv",            # Исключаем ещё один частый вариант названия виртуального окружения.
    "env",             # Исключаем ещё один возможный вариант виртуального окружения.
    ".idea",           # Исключаем настройки PyCharm.
    ".vscode",         # Исключаем настройки Visual Studio Code.
}


# Получаем полный путь к самому этому скрипту.
SCRIPT_PATH = Path(__file__).resolve()


# Получаем папку, в которой лежит сам этот скрипт.
ROOT_DIR = SCRIPT_PATH.parent


# Формируем полный путь к итоговому файлу code_dump.txt рядом со скриптом.
OUTPUT_FILE_PATH = ROOT_DIR / OUTPUT_FILE_NAME


# Задаём строку-разделитель, чтобы в итоговом txt визуально отделять один файл от другого.
SEPARATOR = "=" * 100


# Объявляем функцию, которая проверяет, находится ли файл внутри исключённой папки.
def is_inside_excluded_dir(file_path: Path) -> bool:
    # Получаем путь файла относительно папки, где лежит скрипт.
    relative_path = file_path.relative_to(ROOT_DIR)

    # Проходим по каждой части относительного пути: папки и имя файла.
    for path_part in relative_path.parts:
        # Проверяем, совпадает ли очередная часть пути с одной из исключённых папок.
        if path_part in EXCLUDED_DIRS:
            # Возвращаем True, если файл лежит внутри исключённой папки.
            return True

    # Возвращаем False, если файл не находится внутри исключённых папок.
    return False


# Объявляем функцию, которая будет искать все Python-файлы в папке скрипта и подпапках.
def find_python_files() -> list[Path]:
    # Создаём пустой список, куда будем складывать найденные Python-файлы.
    python_files = []

    # Рекурсивно обходим все файлы и папки внутри папки скрипта.
    for file_path in ROOT_DIR.rglob("*"):
        # Пропускаем всё, что не является обычным файлом.
        if not file_path.is_file():
            # Переходим к следующему найденному объекту.
            continue

        # Пропускаем сам этот скрипт, чтобы он не копировал сам себя в дамп.
        if file_path.resolve() == SCRIPT_PATH:
            # Переходим к следующему найденному объекту.
            continue

        # Пропускаем итоговый файл code_dump.txt, если он уже существует.
        if file_path.resolve() == OUTPUT_FILE_PATH.resolve():
            # Переходим к следующему найденному объекту.
            continue

        # Пропускаем файл, если он лежит внутри исключённой папки.
        if is_inside_excluded_dir(file_path):
            # Переходим к следующему найденному объекту.
            continue

        # Пропускаем файл, если его расширение не .py.
        if file_path.suffix.lower() != ".py":
            # Переходим к следующему найденному объекту.
            continue

        # Добавляем найденный Python-файл в список.
        python_files.append(file_path)

    # Сортируем список файлов по относительному пути, чтобы порядок в дампе был стабильным и понятным.
    python_files.sort(key=lambda path: str(path.relative_to(ROOT_DIR)).lower())

    # Возвращаем готовый список найденных Python-файлов.
    return python_files


# Объявляем функцию для безопасного чтения текстового файла.
def read_python_file(file_path: Path) -> str:
    # Пробуем прочитать файл в стандартной кодировке UTF-8.
    try:
        # Возвращаем содержимое файла как строку.
        return file_path.read_text(encoding="utf-8")

    # Обрабатываем случай, когда файл сохранён не в UTF-8.
    except UnicodeDecodeError:
        # Читаем файл в кодировке Windows-1251, заменяя нечитаемые символы специальными знаками.
        return file_path.read_text(encoding="cp1251", errors="replace")

    # Обрабатываем любые другие ошибки чтения файла.
    except Exception as error:
        # Возвращаем текст ошибки прямо в дамп, чтобы было понятно, какой файл не прочитался.
        return f"# ОШИБКА ЧТЕНИЯ ФАЙЛА: {error}\n"


# Объявляем главную функцию скрипта.
def main() -> None:
    # Получаем список всех найденных Python-файлов.
    python_files = find_python_files()

    # Открываем итоговый файл code_dump.txt на запись в кодировке UTF-8.
    with OUTPUT_FILE_PATH.open("w", encoding="utf-8", newline="\n") as dump_file:
        # Записываем служебный заголовок всего дампа.
        dump_file.write(f"Сборка Python-кода из папки: {ROOT_DIR}\n")

        # Записываем количество найденных Python-файлов.
        dump_file.write(f"Найдено Python-файлов: {len(python_files)}\n")

        # Записываем пустую строку после общего заголовка.
        dump_file.write("\n")

        # Проходим по каждому найденному Python-файлу.
        for file_path in python_files:
            # Получаем путь файла относительно папки, где лежит скрипт.
            relative_path = file_path.relative_to(ROOT_DIR)

            # Читаем содержимое очередного Python-файла.
            file_content = read_python_file(file_path)

            # Записываем большой разделитель перед очередным файлом.
            dump_file.write(f"{SEPARATOR}\n")

            # Записываем заголовок с относительным путём до файла.
            dump_file.write(f"ФАЙЛ: {relative_path}\n")

            # Записываем ещё один разделитель после заголовка файла.
            dump_file.write(f"{SEPARATOR}\n")

            # Записываем пустую строку перед кодом файла.
            dump_file.write("\n")

            # Записываем сам код файла в общий txt.
            dump_file.write(file_content)

            # Проверяем, заканчивается ли содержимое файла переносом строки.
            if not file_content.endswith("\n"):
                # Добавляем перенос строки, чтобы следующий заголовок не прилип к последней строке кода.
                dump_file.write("\n")

            # Добавляем пустую строку после содержимого файла.
            dump_file.write("\n\n")

    # Выводим в консоль сообщение об успешном завершении работы.
    print(f"Готово: создан файл {OUTPUT_FILE_PATH}")

    # Выводим в консоль количество файлов, попавших в дамп.
    print(f"Собрано Python-файлов: {len(python_files)}")


# Проверяем, что файл запущен напрямую, а не импортирован как модуль.
if __name__ == "__main__":
    # Запускаем главную функцию скрипта.
    main()