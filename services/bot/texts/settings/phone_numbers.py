# services/bot/texts/settings/phone_numbers.py

"""
–§–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–∏–µ —Ç–µ–∫—Å—Ç–∞ —ç–∫—Ä–∞–Ω–∞ ¬´–ù–∞—Å—Ç—Ä–æ–π–∫–∏ –Ω–æ–º–µ—Ä–æ–≤ —Ç–µ–ª–µ—Ñ–æ–Ω–æ–≤¬ª.

–ò–¥–µ—è:
- –∏—Å–ø–æ–ª—å–∑—É–µ–º –ø–æ—á—Ç–∏ —Ç–∞–∫–æ–π –∂–µ —Ñ–æ—Ä–º–∞—Ç, –∫–∞–∫ –≤ ¬´–õ–∏—á–Ω–æ–º –∫–∞–±–∏–Ω–µ—Ç–µ¬ª;
- –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –¢–û–õ–¨–ö–û –Ω–æ–º–µ—Ä–∞ —Ç–µ–ª–µ—Ñ–æ–Ω–æ–≤ –∏ –ø—Ä–∏–≤—è–∑–∞–Ω–Ω—ã–µ –∫ –Ω–∏–º –±–∞–Ω–∫–∏;
- —Ä–µ–∫–≤–∏–∑–∏—Ç—ã –í–°–ï–ì–î–ê —Ä–∞—Å–∫—Ä—ã—Ç—ã –ø–æ–ª–Ω–æ—Å—Ç—å—é (–±–µ–∑ –º–∞—Å–∫–∏).
"""

from __future__ import annotations                                   # –†–∞–∑—Ä–µ—à–∞–µ–º "–æ—Ç–ª–æ–∂–µ–Ω–Ω—ã–µ" –∞–Ω–Ω–æ—Ç–∞—Ü–∏–∏ —Ç–∏–ø–æ–≤

from typing import List                                              # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º List –¥–ª—è –∞–Ω–Ω–æ—Ç–∞—Ü–∏–π —Ç–∏–ø–æ–≤

from ...database import get_user                                     # –§—É–Ω–∫—Ü–∏—è –ø–æ–ª—É—á–µ–Ω–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏–∑ —Ñ–∞–π–ª–æ–≤–æ–π "–ë–î"
from ..personal_cabinet import (                                     # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –≤—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–µ –æ–±—ä–µ–∫—Ç—ã –∏–∑ –õ–ö
    PHONE_MARK,                                                      # –≠–º–æ–¥–∑–∏ –ø–µ—Ä–µ–¥ –Ω–æ–º–µ—Ä–æ–º —Ç–µ–ª–µ—Ñ–æ–Ω–∞
    _wrap_with_indent,                                               # –§—É–Ω–∫—Ü–∏—è –∞–∫–∫—É—Ä–∞—Ç–Ω–æ–≥–æ –ø–µ—Ä–µ–Ω–æ—Å–∞ —Å—Ç—Ä–æ–∫ —Å –æ—Ç—Å—Ç—É–ø–∞–º–∏
    _format_bank_title,                                              # –§—É–Ω–∫—Ü–∏—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è —Å—Ç—Ä–æ–∫–∏ –±–∞–Ω–∫–∞
)


async def build_phone_numbers_settings_text(
    user_id: int,                                                    # ID –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤ Telegram (–∫–ª—é—á JSON-—Ñ–∞–π–ª–∞)
) -> str:
    """
    –°—Ñ–æ—Ä–º–∏—Ä–æ–≤–∞—Ç—å —Ç–µ–∫—Å—Ç —ç–∫—Ä–∞–Ω–∞ ¬´–ù–∞—Å—Ç—Ä–æ–π–∫–∏ –Ω–æ–º–µ—Ä–æ–≤ —Ç–µ–ª–µ—Ñ–æ–Ω–æ–≤¬ª –¥–ª—è –∑–∞–¥–∞–Ω–Ω–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.

    –û—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏:
    - –±–µ—Ä—ë–º —Ç–æ–ª—å–∫–æ —Ç–µ–ª–µ—Ñ–æ–Ω—ã (user.phones);
    - –¥–ª—è –∫–∞–∂–¥–æ–≥–æ —Ç–µ–ª–µ—Ñ–æ–Ω–∞ –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –ø–æ–ª–Ω—ã–π –Ω–æ–º–µ—Ä;
    - –Ω–∏–∂–µ —Å–ø–∏—Å–∫–æ–º –≤—ã–≤–æ–¥–∏–º –±–∞–Ω–∫–∏, –∫–∞–∫ –≤ –õ–ö;
    - —Ä–∞–∑–º–µ—Ç–∫–∞ ‚Äî Markdown.
    """

    user = await get_user(user_id)                                         # –ó–∞–≥—Ä—É–∂–∞–µ–º (–∏–ª–∏ —Å–æ–∑–¥–∞—ë–º) –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏–∑ "–ë–î"

    lines: List[str] = []                                            # –°–ø–∏—Å–æ–∫ —Å—Ç—Ä–æ–∫ –±—É–¥—É—â–µ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è

    # --- –ó–∞–≥–æ–ª–æ–≤–æ–∫ —ç–∫—Ä–∞–Ω–∞ --- #

    # –î–µ–ª–∞–µ–º —Ç–æ—Ç –∂–µ –∑–∞–≥–æ–ª–æ–≤–æ–∫, —á—Ç–æ –∏ –≤ –õ–ö, —á—Ç–æ–±—ã —Ç–µ–∫—Å—Ç –≤—ã–≥–ª—è–¥–µ–ª "–∑–Ω–∞–∫–æ–º–æ".
    #lines.append("*–í–∞—à–∏ –ø–ª–∞—Ç–µ–∂–Ω—ã–µ —Ä–µ–∫–≤–∏–∑–∏—Ç—ã:*")                      # –ü–µ—Ä–≤–∞—è —Å—Ç—Ä–æ–∫–∞ ‚Äî –∂–∏—Ä–Ω—ã–º (Markdown)
    #lines.append("")                                                 # –ü—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞-—Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å

    # --- –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –≤–æ–æ–±—â–µ —Ç–µ–ª–µ—Ñ–æ–Ω—ã --- #

    has_phones = bool(user.phones)                                   # True, –µ—Å–ª–∏ —Å–ª–æ–≤–∞—Ä—å —Ç–µ–ª–µ—Ñ–æ–Ω–æ–≤ –Ω–µ –ø—É—Å—Ç–æ–π

    if not has_phones:                                               # –ï—Å–ª–∏ —Ç–µ–ª–µ—Ñ–æ–Ω–æ–≤ –Ω–µ—Ç —Å–æ–≤—Å–µ–º
        # –ü–∏—à–µ–º –ø—Ä–æ—Å—Ç–æ–µ –∏ –ø–æ–Ω—è—Ç–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ
        lines.append("–£ –≤–∞—Å –ø–æ–∫–∞ –Ω–µ—Ç —Å–æ—Ö—Ä–∞–Ω—ë–Ω–Ω—ã—Ö –Ω–æ–º–µ—Ä–æ–≤ —Ç–µ–ª–µ—Ñ–æ–Ω–æ–≤.")  # –°–æ–æ–±—â–∞–µ–º, —á—Ç–æ –Ω–æ–º–µ—Ä–æ–≤ –Ω–µ—Ç

        # –ü—Ä–æ–≥–æ–Ω—è–µ–º –≤—Å–µ —Å—Ç—Ä–æ–∫–∏ —á–µ—Ä–µ–∑ –ø–µ—Ä–µ–Ω–æ—Å—á–∏–∫, —á—Ç–æ–±—ã –Ω–µ —Å–ª–æ–º–∞—Ç—å –æ—Ç—Å—Ç—É–ø—ã (—Ö–æ—Ç—è –∏—Ö —Ç—É—Ç –∏ –Ω–µ—Ç)
        wrapped_lines = [_wrap_with_indent(line) for line in lines]  # –î–ª—è –∫–∞–∂–¥–æ–π —Å—Ç—Ä–æ–∫–∏ –ø—Ä–∏–º–µ–Ω—è–µ–º –ø–µ—Ä–µ–Ω–æ—Å
        return "\n".join(wrapped_lines)                              # –°–∫–ª–µ–∏–≤–∞–µ–º —Å—Ç—Ä–æ–∫–∏ –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Ç–µ–∫—Å—Ç

    # --- –ë–ª–æ–∫ —Ç–µ–ª–µ—Ñ–æ–Ω–æ–≤ (–ø–æ –∞–Ω–∞–ª–æ–≥–∏–∏ —Å –õ–ö, –Ω–æ –±–µ–∑ –º–∞—Å–æ–∫ –∏ –±–µ–∑ –∫–∞—Ä—Ç) --- #
    # user.phones ‚Äî —Å–ª–æ–≤–∞—Ä—å –≤–∏–¥–∞: { "–Ω–æ–º–µ—Ä": PhoneData }

    for phone, phone_data in user.phones.items():                    # –ò–¥—ë–º –ø–æ –≤—Å–µ–º –ø–∞—Ä–∞–º (–Ω–æ–º–µ—Ä, –¥–∞–Ω–Ω—ã–µ —Ç–µ–ª–µ—Ñ–æ–Ω–∞)
        # –í –Ω–∞—Å—Ç—Ä–æ–π–∫–∞—Ö –Ω–æ–º–µ—Ä–æ–≤ —Ä–µ–∫–≤–∏–∑–∏—Ç—ã –í–°–ï–ì–î–ê –ø–æ–∫–∞–∑—ã–≤–∞–µ–º —Ü–µ–ª–∏–∫–æ–º,
        # –ø–æ—ç—Ç–æ–º—É —Å—Ä–∞–∑—É –ø–æ–¥—Å—Ç–∞–≤–ª—è–µ–º –ø–æ–ª–Ω—ã–π –Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞.
        phone_line = f"*{PHONE_MARK} –¢–µ–ª–µ—Ñ–æ–Ω {phone}:*"              # –ü—Ä–∏–º–µ—Ä: "*üì± –¢–µ–ª–µ—Ñ–æ–Ω +79991234567:*"

        lines.append(phone_line)                                     # –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç—Ä–æ–∫—É —Ç–µ–ª–µ—Ñ–æ–Ω–∞ –≤ –æ–±—â–∏–π —Å–ø–∏—Å–æ–∫

        # –ü–æ–ª—É—á–∞–µ–º —Å–ø–∏—Å–æ–∫ –±–∞–Ω–∫–æ–≤, –ø—Ä–∏–≤—è–∑–∞–Ω–Ω—ã—Ö –∫ —ç—Ç–æ–º—É –Ω–æ–º–µ—Ä—É
        banks = list(phone_data.banks or [])                         # –°–ø–∏—Å–æ–∫ –∫–æ–¥–æ–≤ –±–∞–Ω–∫–æ–≤ (–∏–ª–∏ –ø—É—Å—Ç–æ–π —Å–ø–∏—Å–æ–∫)
        main_bank = phone_data.main_bank                             # –ö–æ–¥ –æ—Å–Ω–æ–≤–Ω–æ–≥–æ –±–∞–Ω–∫–∞ (–∏–ª–∏ None)

        # --- –ï—Å–ª–∏ –±–∞–Ω–∫–æ–≤ –∫ —Ç–µ–ª–µ—Ñ–æ–Ω—É –Ω–µ –ø—Ä–∏–≤—è–∑–∞–Ω–æ --- #

        if not banks:                                                # –ï—Å–ª–∏ —Å–ø–∏—Å–æ–∫ –±–∞–Ω–∫–æ–≤ –ø—É—Å—Ç
            lines.append("    (–±–∞–Ω–∫–∏ –Ω–µ –≤—ã–±—Ä–∞–Ω—ã)")                   # –ü–∏—à–µ–º –ø–æ—è—Å–Ω–µ–Ω–∏–µ —Å –æ—Ç—Å—Ç—É–ø–æ–º –≤ 4 –ø—Ä–æ–±–µ–ª–∞
            lines.append("")                                         # –ü—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞-—Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å –º–µ–∂–¥—É —Ç–µ–ª–µ—Ñ–æ–Ω–∞–º–∏
            continue                                                 # –ü–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Å–ª–µ–¥—É—é—â–µ–º—É –Ω–æ–º–µ—Ä—É —Ç–µ–ª–µ—Ñ–æ–Ω–∞

        # --- –§–æ—Ä–º–∏—Ä—É–µ–º —Å—Ç—Ä–æ–∫–∏ –ø–æ –∫–∞–∂–¥–æ–º—É –±–∞–Ω–∫—É --- #

        for bank_code in banks:                                      # –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –∫–æ–¥—ã –±–∞–Ω–∫–æ–≤
            # –ë–∞–Ω–∫ –æ—Å–Ω–æ–≤–Ω–æ–π, –µ—Å–ª–∏ main_bank –∑–∞–¥–∞–Ω –∏ —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Å —Ç–µ–∫—É—â–∏–º –∫–æ–¥–æ–º
            is_main = (main_bank is not None) and (bank_code == main_bank)

            # –°—Ç—Ä–æ–∫—É –±–∞–Ω–∫–∞ —Ñ–æ—Ä–º–∏—Ä—É–µ–º —á–µ—Ä–µ–∑ —Ç—É –∂–µ —Ñ—É–Ω–∫—Ü–∏—é, —á—Ç–æ –∏ –≤ –õ–ö
            bank_line = _format_bank_title(bank_code, is_main)      # –ù–∞–ø—Ä–∏–º–µ—Ä: "    *–¢-–ë–∞–Ω–∫ (–æ—Å–Ω–æ–≤–Ω–æ–π)*"

            lines.append(bank_line)                                 # –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç—Ä–æ–∫—É –±–∞–Ω–∫–∞ –≤ –æ–±—â–∏–π —Å–ø–∏—Å–æ–∫

        lines.append("")                                            # –ü—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞ –º–µ–∂–¥—É –±–ª–æ–∫–∞–º–∏ —Ä–∞–∑–Ω—ã—Ö —Ç–µ–ª–µ—Ñ–æ–Ω–æ–≤

    # --- –ü—Ä–∏–º–µ–Ω—è–µ–º –ø–µ—Ä–µ–Ω–æ—Å—ã —Å—Ç—Ä–æ–∫ —Å —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ–º –æ—Ç—Å—Ç—É–ø–æ–≤ --- #

    wrapped_lines = [                                                # –î–ª—è –∫–∞–∂–¥–æ–π –ª–æ–≥–∏—á–µ—Å–∫–æ–π —Å—Ç—Ä–æ–∫–∏
        _wrap_with_indent(line)                                      # –¥–µ–ª–∞–µ–º –ø–µ—Ä–µ–Ω–æ—Å —Å —É—á—ë—Ç–æ–º –æ—Ç—Å—Ç—É–ø–æ–≤
        for line in lines
    ]

    # –°–∫–ª–µ–∏–≤–∞–µ–º –≤—Å–µ —Å—Ç—Ä–æ–∫–∏ –≤ –æ–¥–∏–Ω —Ç–µ–∫—Å—Ç —á–µ—Ä–µ–∑ '\n' –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º
    return "\n".join(wrapped_lines)                                  # –ì–æ—Ç–æ–≤—ã–π —Ç–µ–∫—Å—Ç —ç–∫—Ä–∞–Ω–∞ ¬´–ù–∞—Å—Ç—Ä–æ–π–∫–∏ –Ω–æ–º–µ—Ä–æ–≤ —Ç–µ–ª–µ—Ñ–æ–Ω–æ–≤¬ª
