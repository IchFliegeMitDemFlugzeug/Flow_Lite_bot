"""
Формирование текста экрана «Настройки банковских карт».

Задача:
- показать только банковские карты пользователя;
- форматировать их так же, как в «Личном кабинете» при включённом show_details;
- для каждой карты вывести:
  - строку с номером карты (моноширинный шрифт, эмодзи 💳),
  - при наличии — банк карты,
  - при наличии — платёжную систему,
  - пустую строку-разделитель между картами.
"""

from __future__ import annotations                                   # Разрешаем отложенные аннотации типов (удобно для type hints)

from typing import List                                              # Импортируем List для аннотаций типов

from ...database import get_user                                     # Функция для получения пользователя из файловой "БД"
from ..personal_cabinet import (                                     # Импортируем общие вспомогательные функции и константы
    CARD_MARK,                                                       # Эмодзи 💳 для обозначения карты
    _wrap_with_indent,                                               # Функция переноса строк с сохранением отступов
    _format_bank_title,                                              # Функция форматирования строки банка
)


def build_add_card_enter_number_text() -> str:
    """
    Сформировать текст первого экрана добавления банковской карты.

    Этот экран соответствует схеме:
    - заголовок «Добавление банковской карты» находится на картинке-заголовке;
    - в тексте просим отправить номер карты;
    - отдельно предупреждаем, что по этому номеру будут переводить деньги.
    """

    lines: List[str] = []                                            # Создаём список строк будущего сообщения
    lines.append("Отправьте номер Вашей банковской карты.")          # Добавляем основную просьбу из схемы
    lines.append("")                                                 # Добавляем пустую строку для визуального разделения
    lines.append(                                                     # Добавляем предупреждение из схемы
        "⚠️ Будьте внимательны при вводе номера! "
        "По этому номеру карты Вам будут переводить денежные средства!"
    )

    wrapped_lines = [                                                # Готовим список строк с единым форматированием переносов
        _wrap_with_indent(line)                                      # Применяем общий перенос строки из проекта
        for line in lines                                            # Проходим по каждой строке сообщения
    ]

    return "\n".join(wrapped_lines)                                  # Возвращаем готовый текст одним Markdown-сообщением


def build_add_card_invalid_number_text() -> str:
    """
    Сформировать текст ошибки, если пользователь отправил некорректный номер карты.
    """

    lines: List[str] = []                                            # Создаём список строк будущего сообщения
