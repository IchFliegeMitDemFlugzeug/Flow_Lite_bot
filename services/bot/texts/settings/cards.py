async def build_cards_settings_text(
    user_id: int,                                                    # ID пользователя (одновременно имя JSON-файла в БД)
) -> str:
    """
    Сформировать текст экрана «Настройки банковских карт» для заданного пользователя.

    Особенности:
    - показываем ТОЛЬКО банковские карты (телефоны здесь не выводим);
    - всегда отображаем номера карт полностью (как в ЛК при show_details=True);
    - используем Markdown (жирный текст, моноширинный шрифт для номера карты).
    """

    user = await get_user(user_id)                                         # Загружаем (или создаём) пользователя по user_id

    lines: List[str] = []                                            # Список строк будущего текста сообщения

    # --- Заголовок экрана --- #

    #lines.append("*Ваши платежные реквизиты:*")                      # Общий заголовок для блока реквизитов (жирным)
    #lines.append("")                                                 # Пустая строка-разделитель после заголовка

    # --- Забираем список карт пользователя --- #
    # По договорённости структура user.cards:
    #   user.cards: Dict[str, CardData]
    # где CardData содержит:
    #   - number (как ключ словаря)
    #   - bank (код банка, строка или None)
    #   - payment_system (строка или None)

    user_cards = getattr(user, "cards", {})                          # Аккуратно берём словарь карт (или {} для старых версий)
    has_cards: bool = bool(user_cards)                               # Флаг — есть ли вообще карты у пользователя

    if not has_cards:                                                # Если карт нет
        # Пишем понятное сообщение о том, что реквизитов-карт ещё нет
        lines.append("У вас пока нет сохранённых банковских карт.")  # Сообщение, если ни одной карты не сохранено

        # Прогоняем строки через переносчик для единообразия форматирования
        wrapped_lines = [_wrap_with_indent(line) for line in lines]  # Переносим каждую строку с учётом отступов
        return "\n".join(wrapped_lines)                              # Склеиваем строки через '\n' и возвращаем текст

    # --- Формируем блок по каждой карте --- #

    for card_number, card_data in user_cards.items():                # Итерируемся по парам (номер карты, данные карты)
        card_number_str = str(card_number)                           # Номер карты приводим к строке на всякий случай

        # В настройках карт всегда показываем номер полностью.
        # Для удобства копирования выводим его в моноширинном шрифте (Markdown-код `номер`).
        number_display = f"`{card_number_str}`"                      # Оборачиваем номер в обратные апострофы

        # Основная строка карты:
        # Пример: "*💳 Карта* `5555444433331111`"
        card_line = f"*{CARD_MARK} Карта* {number_display}"          # Собираем строку с эмодзи, меткой "Карта" и номером

        lines.append(card_line)                                      # Добавляем строку карты в общий список

        # --- Информация о банке карты (если задан) --- #

        card_bank_code = getattr(card_data, "bank", None)            # Код банка, привязанного к карте (может быть None)
        if card_bank_code:                                           # Если банк указан
            # Используем общий форматировщик банков.
            # Здесь не нужен статус "(основной)", поэтому is_main=False.
            card_bank_line = _format_bank_title(                     # Формируем строку банка
                bank_code=card_bank_code,                            # Передаём код банка
                is_main=False,                                       # Для карты просто обычный банк, не "основной"
            )
            lines.append(card_bank_line)                             # Добавляем строку с банком карты

        # --- Информация о платёжной системе (если задана) --- #

        card_payment_system = getattr(card_data, "payment_system", None)  # Платёжная система карты (строка или None)
        if card_payment_system:                                      # Если платёжная система указана
            # Пример строки: "    MIR" или "    Visa"
            ps_line = f"    {card_payment_system}"                   # 4 пробела + название платёжной системы
            lines.append(ps_line)                                    # Добавляем строку платёжной системы

        # Пустая строка-разделитель между картами
        lines.append("")                                             # Отделяем блоки разных карт визуально

    # --- Применяем переносы строк с учётом отступов --- #

    wrapped_lines = [                                                # Для каждой логической строки
        _wrap_with_indent(line)                                      # делаем перенос с сохранением префикса отступа
        for line in lines
    ]

    # Склеиваем все строки в один текст через '\n' и возвращаем
    return "\n".join(wrapped_lines)                                  # Возвращаем итоговый текст экрана настроек карт
