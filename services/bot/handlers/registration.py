    #   - –≤ main_bank –ø–µ—Ä–µ–¥–∞—ë–º —Ç–æ–ª—å–∫–æ —á—Ç–æ –≤—ã–±—Ä–∞–Ω–Ω—ã–π –∫–æ–¥ –±–∞–Ω–∫–∞, —á—Ç–æ–±—ã –Ω–∞ –Ω—ë–º –ø–æ—è–≤–∏–ª–∞—Å—å "–≥–∞–ª–æ—á–∫–∞".
