    bank_code = action                           # –≤–æ—Å–ø—Ä–∏–Ω–∏–º–∞–µ–º action –∫–∞–∫ –∫–æ–¥ –±–∞–Ω–∫–∞, –Ω–∞–ø—Ä–∏–º–µ—Ä "sber"

