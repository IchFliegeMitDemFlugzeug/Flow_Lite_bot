        selected_banks.remove(bank_code)         # —É–±–∏—Ä–∞–µ–º –µ–≥–æ –∏–∑ —Å–ø–∏—Å–∫–∞ ‚Äî –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å "–æ—Ç–∂–∞–ª" —ç—Ç–æ—Ç –±–∞–Ω–∫
