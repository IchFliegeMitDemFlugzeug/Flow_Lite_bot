            reply_markup=build_request_phone_keyboard(),  # –ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ —Å –∫–Ω–æ–ø–∫–æ–π "–û—Ç–ø—Ä–∞–≤–∏—Ç—å –Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞"
            parse_mode="Markdown"                    # –£–∫–∞–∑—ã–≤–∞–µ–º Markdown, —á—Ç–æ–±—ã *–∂–∏—Ä–Ω—ã–π* –∏ —Ç.–ø. —Ä–∞–±–æ—Ç–∞–ª–∏
