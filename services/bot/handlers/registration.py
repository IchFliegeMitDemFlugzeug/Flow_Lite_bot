    # –ï—Å–ª–∏ —Å—é–¥–∞ –¥–æ—à–ª–∏ ‚Äî –∑–Ω–∞—á–∏—Ç, action –Ω–µ "back" –∏ –Ω–µ "next".
    # –°—á–∏—Ç–∞–µ–º, —á—Ç–æ —ç—Ç–æ –∫–æ–¥ –±–∞–Ω–∫–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, "sber", "tbank", "vtb" –∏ —Ç.–ø.).
    bank_code: str = action

