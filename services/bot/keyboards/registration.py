from aiogram.utils.keyboard import InlineKeyboardBuilder  # –£–¥–æ–±–Ω—ã–π –±–∏–ª–¥–µ—Ä –¥–ª—è –∏–Ω–ª–∞–π–Ω-–∫–ª–∞–≤–∏–∞—Ç—É—Ä


def build_request_phone_keyboard() -> ReplyKeyboardMarkup:
    """
    –ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –¥–ª—è –∑–∞–ø—Ä–æ—Å–∞ –Ω–æ–º–µ—Ä–∞ —Ç–µ–ª–µ—Ñ–æ–Ω–∞ (–®–∞–≥ 1 –∏–∑ 3).
    –ó–¥–µ—Å—å –æ–¥–Ω–∞ –±–æ–ª—å—à–∞—è –∫–Ω–æ–ø–∫–∞ ¬´–û—Ç–ø—Ä–∞–≤–∏—Ç—å –Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞¬ª,
    –∫–æ—Ç–æ—Ä–∞—è –∑–∞–ø—Ä–∞—à–∏–≤–∞–µ—Ç –∫–æ–Ω—Ç–∞–∫—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.
    """
    # –°–æ–∑–¥–∞—ë–º –∫–Ω–æ–ø–∫—É –¥–ª—è –∑–∞–ø—Ä–æ—Å–∞ –∫–æ–Ω—Ç–∞–∫—Ç–∞.
    # KeyboardButton ‚Äî –æ–±—ä–µ–∫—Ç –æ–ø–∏—Å—ã–≤–∞–µ—Ç –æ–¥–Ω—É –∫–Ω–æ–ø–∫—É –æ–±—ã—á–Ω–æ–π –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã.
    # –ê—Ä–≥—É–º–µ–Ω—Ç text ‚Äî —Ç–µ–∫—Å—Ç –Ω–∞ –∫–Ω–æ–ø–∫–µ.
    # –ê—Ä–≥—É–º–µ–Ω—Ç request_contact=True –≥–æ–≤–æ—Ä–∏—Ç Telegram'—É –æ—Ç–ø—Ä–∞–≤–∏—Ç—å –∫–æ–Ω—Ç–∞–∫—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –ø—Ä–∏ –Ω–∞–∂–∞—Ç–∏–∏.
    contact_button = KeyboardButton(
        text="–û—Ç–ø—Ä–∞–≤–∏—Ç—å –Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞",  # –¢–µ–∫—Å—Ç –Ω–∞ –∫–Ω–æ–ø–∫–µ
        request_contact=True              # –§–ª–∞–≥: –ø—Ä–∏ –Ω–∞–∂–∞—Ç–∏–∏ –æ—Ç–ø—Ä–∞–≤–∏—Ç—å –∫–æ–Ω—Ç–∞–∫—Ç
    )

    # –°–æ–∑–¥–∞—ë–º —Ä–∞—Å–∫–ª–∞–¥–∫—É –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã.
    # ReplyKeyboardMarkup ‚Äî –æ–±—ã—á–Ω–∞—è –∫–ª–∞–≤–∏–∞—Ç—É—Ä–∞, –∫–æ—Ç–æ—Ä–∞—è –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç—Å—è –ø–æ–¥ –ø–æ–ª–µ–º –≤–≤–æ–¥–∞.
    keyboard = ReplyKeyboardMarkup(
        keyboard=[[contact_button]],  # –ú–∞—Å—Å–∏–≤ —Ä—è–¥–æ–≤, –∫–∞–∂–¥—ã–π —Ä—è–¥ ‚Äî —Å–ø–∏—Å–æ–∫ –∫–Ω–æ–ø–æ–∫
        resize_keyboard=True,         # resize_keyboard=True ‚Äî Telegram –ø–æ–¥–≥–æ–Ω–∏—Ç —Ä–∞–∑–º–µ—Ä –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã
        one_time_keyboard=True        # one_time_keyboard=True ‚Äî –∫–ª–∞–≤–∏–∞—Ç—É—Ä–∞ —Å–∫—Ä–æ–µ—Ç—Å—è –ø–æ—Å–ª–µ –Ω–∞–∂–∞—Ç–∏—è
    )

    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –≥–æ—Ç–æ–≤—É—é –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É
    return keyboard


# –°–ø–∏—Å–æ–∫ "–ª–æ–≥–∏—á–µ—Å–∫–∏—Ö" –±–∞–Ω–∫–æ–≤ ‚Äî –∏—Å–ø–æ–ª—å–∑—É–µ–º –∫–æ–¥—ã, –∞ –ø–æ–¥–ø–∏—Å—å —Ñ–æ—Ä–º–∏—Ä—É–µ–º –≤ –æ—Ç–¥–µ–ª—å–Ω–æ–º —Å–ª–æ–≤–∞—Ä–µ.
# –≠—Ç–æ —É–¥–æ–±–Ω–æ, —á—Ç–æ–±—ã –Ω–µ —Ö–∞—Ä–¥–∫–æ–¥–∏—Ç—å —Å—Ç—Ä–æ–∫–∏ –≤–æ –≤—Å–µ—Ö –º–µ—Å—Ç–∞—Ö.
BANK_CODES = [
    "sber",
    "tbank",
    "vtb",
    "alfa",
    "mkb",
    "psb",
    "gazprom",
    "pochtab",
    "rshb",
    "sovcom",
    "ozon",
    "wb",
]

# –ß–∏—Ç–∞–µ–º—ã–µ –Ω–∞–∑–≤–∞–Ω–∏—è –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –≤ –∫–Ω–æ–ø–∫–∞—Ö.
BANK_LABELS = {
    "sber": "–°–±–µ—Ä",
    "tbank": "–¢-–±–∞–Ω–∫",
    "vtb": "–í–¢–ë",
    "alfa": "–ê–ª—å—Ñ–∞",
    "mkb": "–ú–ö–ë",
    "psb": "–ü–°–ë",
    "gazprom": "–ì–∞–∑–ü—Ä–æ–º",
    "pochtab": "–ü–æ—á—Ç–∞–ë",
    "rshb": "–†–°–•–ë",
    "sovcom": "–°–æ–≤–∫–æ–º",
    "ozon": "OZON",
    "wb": "WB",
}


def build_bank_choice_keyboard(selected_banks: list[str]) -> InlineKeyboardMarkup:
    """
    –ò–Ω–ª–∞–π–Ω-–∫–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –¥–ª—è —ç–∫—Ä–∞–Ω–∞ ¬´–í—ã–±–æ—Ä –±–∞–Ω–∫–æ–≤¬ª (–®–∞–≥ 2 –∏–∑ 3).
    selected_banks ‚Äî —Å–ø–∏—Å–æ–∫ –∫–æ–¥–æ–≤ –±–∞–Ω–∫–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ —É–∂–µ –≤—ã–±—Ä–∞–Ω—ã –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º.
    –î–ª—è –≤—ã–±—Ä–∞–Ω–Ω—ã—Ö –±–∞–Ω–∫–æ–≤ –¥–æ–±–∞–≤–ª—è–µ–º –≥–∞–ª–æ—á–∫—É ¬´‚úî¬ª –ø–µ—Ä–µ–¥ –Ω–∞–∑–≤–∞–Ω–∏–µ–º.
    """

    # InlineKeyboardBuilder ‚Äî —É–¥–æ–±–Ω—ã–π –∫–ª–∞—Å—Å, —á—Ç–æ–±—ã —Å—Ç—Ä–æ–∏—Ç—å –∏–Ω–ª–∞–π–Ω-–∫–ª–∞–≤–∏–∞—Ç—É—Ä—É –ø–æ—Å—Ç—Ä–æ—á–Ω–æ.
    builder = InlineKeyboardBuilder()

    # –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è: –±–µ—Ä—ë–º –∫–æ–¥ –±–∞–Ω–∫–∞ –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –ø–æ–¥–ø–∏—Å—å —Å –≥–∞–ª–æ—á–∫–æ–π –∏–ª–∏ –±–µ–∑.
    def make_label(code: str) -> str:
        # –ü–æ–ª—É—á–∞–µ–º —á–∏—Ç–∞–µ–º–æ–µ –Ω–∞–∑–≤–∞–Ω–∏–µ –±–∞–Ω–∫–∞ –ø–æ –∫–æ–¥—É
        base_label = BANK_LABELS[code]
        # –ï—Å–ª–∏ –±–∞–Ω–∫ –≤—ã–±—Ä–∞–Ω (–µ—Å—Ç—å –≤ —Å–ø–∏—Å–∫–µ selected_banks), –¥–æ–±–∞–≤–ª—è–µ–º –≤ –Ω–∞—á–∞–ª–æ ¬´‚úî ¬ª
        if code in selected_banks:
            return f"‚úÖ {base_label}"
        # –ï—Å–ª–∏ –Ω–µ –≤—ã–±—Ä–∞–Ω ‚Äî –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –Ω–∞–∑–≤–∞–Ω–∏–µ –∫–∞–∫ –µ—Å—Ç—å
        return base_label

    # –¢–µ–ø–µ—Ä—å –Ω–∞–º –Ω—É–∂–Ω–æ —Ä–∞–∑–ª–æ–∂–∏—Ç—å –±–∞–Ω–∫–∏ –ø–æ —Ä—è–¥–∞–º –∫–∞–∫ –≤ —Å—Ö–µ–º–µ.
    # –î–ª—è –Ω–∞–≥–ª—è–¥–Ω–æ—Å—Ç–∏ —Å–¥–µ–ª–∞–µ–º —è–≤–Ω–æ–µ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ.
    rows: list[list[str]] = [
        ["sber", "tbank", "vtb"],       # 1 —Ä—è–¥: –°–±–µ—Ä | T-–±–∞–Ω–∫ | –í–¢–ë
        ["alfa", "mkb", "psb"],         # 2 —Ä—è–¥: –ê–ª—å—Ñ–∞ | –ú–ö–ë | –ü–°–ë
        ["gazprom", "pochtab", "rshb"], # 3 —Ä—è–¥: –ì–∞–∑–ü—Ä–æ–º | –ü–æ—á—Ç–∞–ë | –†–°–•–ë
        ["sovcom", "ozon", "wb"],       # 4 —Ä—è–¥: –°–æ–≤–∫–æ–º | OZON | WB
    ]

    # –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ —Ä—è–¥–∞–º –∏ –ø–æ –±–∞–Ω–∫–∞–º –≤–Ω—É—Ç—Ä–∏ —Ä—è–¥–∞.
    for row in rows:
        # –í –±–∏–ª–¥–µ—Ä –¥–æ–±–∞–≤–ª—è–µ–º —Å—Ä–∞–∑—É –Ω–µ—Å–∫–æ–ª—å–∫–æ –∫–Ω–æ–ø–æ–∫ –≤ –æ–¥–∏–Ω —Ä—è–¥.
        builder.row(
            *[
                InlineKeyboardButton(
                    text=make_label(code),  # –¢–µ–∫—Å—Ç –∫–Ω–æ–ø–∫–∏ ‚Äî —Å —É—á—ë—Ç–æ–º –≥–∞–ª–æ—á–∫–∏
                    callback_data=f"bank:{code}"  # callback_data ‚Äî —Å—Ç—Ä–æ–∫–∞, –∫–æ—Ç–æ—Ä—É—é –º—ã –±—É–¥–µ–º –ø–∞—Ä—Å–∏—Ç—å –≤ —Ö—ç–Ω–¥–ª–µ—Ä–µ
                )
                for code in row
            ]
        )

    # –î–æ–±–∞–≤–ª—è–µ–º –æ—Ç–¥–µ–ª—å–Ω—É—é –∫–Ω–æ–ø–∫—É ¬´–ù–µ—Ç –Ω—É–∂–Ω–æ–≥–æ –±–∞–Ω–∫–∞ üòü¬ª –≤ —Å–≤–æ–π —Ä—è–¥.
    builder.row(
        InlineKeyboardButton(
            text="–ù–µ—Ç –Ω—É–∂–Ω–æ–≥–æ –±–∞–Ω–∫–∞ üòü",   # –¢–µ–∫—Å—Ç –Ω–∞ –∫–Ω–æ–ø–∫–µ
            callback_data="bank:no_such"  # callback_data ‚Äî —Å–ø–µ—Ü–∏–∞–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –¥–ª—è –æ—Ç–¥–µ–ª—å–Ω–æ–≥–æ —Ö—ç–Ω–¥–ª–µ—Ä–∞
        )
    )

    # –î–æ–±–∞–≤–ª—è–µ–º –∫–Ω–æ–ø–∫—É ¬´–î–∞–ª–µ–µ ‚û°¬ª –≤ –æ—Ç–¥–µ–ª—å–Ω—ã–π —Ä—è–¥.
    builder.row(
        InlineKeyboardButton(
            text="–î–∞–ª–µ–µ ‚û°",           # –¢–µ–∫—Å—Ç –∫–Ω–æ–ø–∫–∏
            callback_data="bank:next" # callback_data ‚Äî –∫–æ–º–∞–Ω–¥–∞, —á—Ç–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∑–∞–≤–µ—Ä—à–∏–ª –≤—ã–±–æ—Ä
        )
    )

    # –ú–µ—Ç–æ–¥ as_markup() –ø—Ä–µ–≤—Ä–∞—â–∞–µ—Ç –±–∏–ª–¥–µ—Ä –≤ –æ–±—ã—á–Ω—ã–π InlineKeyboardMarkup, –∫–æ—Ç–æ—Ä—ã–π —É–∂–µ –º–æ–∂–Ω–æ –ø–µ—Ä–µ–¥–∞–≤–∞—Ç—å –≤ send_message().
    return builder.as_markup()
